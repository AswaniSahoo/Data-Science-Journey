# Newline
print("Hello\nWorld")
# Output:
# Hello
# World

# Tab
print("Hello\tWorld")
# Output: Hello   World

# Backslash
print("This is a backslash: \\")
# Output: This is a backslash: \

# Single Quote
print('It\'s a sunny day')
# Output: It's a sunny day

# Double Quote
print("He said, \"Hello World\"")
# Output: He said, "Hello World"

# Carriage Return
print("Hello\rWorld")
# Output: World

# Backspace
print("Hello\bWorld")
# Output: HellWorld

# Form Feed
print("Hello\fWorld")
# Output: Hello
#        World

# Unicode Characters
print("\u2764")  # Output: ‚ù§
print("\U0001F604")  # Output: üòÄ

# Octal Value
print("\101\102\103")  # Output: ABC

# Hexadecimal Value
print("\x41\x42\x43")  # Output: ABC

